public class MultipleOperations {
    public static void main(String[] args){

        double t = 5;
        double u = 2.5;

        double v = t + u;
        double w = t - u;
        double x = t*u;
        double y = t/u;
        double z = t%u;

        System.out.println("Sum = "+ v + " subtraction ="+w + " multiply=" +x + "division = "+y + " and rem ="+z);
    }
}
